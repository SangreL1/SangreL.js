//**
// Los operadores de comparación se usan para poder comprar...
// ..distintas variables entre si o una variable con otra...
// ...cosa
// */

// Operadores relacionales.

let a = 10;
console.log(a > 5); // Esto imprimirá True en la consola.
console.log(a >= 5); // Esto imprimirá True en la consola.
console.log(a < 5); // Esto imprimirá False en la consola.
console.log(a <= 5); // Esto imprimirá False en la consola.


// Operaodres de igualdad.

console.log(a == 10); // La variable A es igual a 10. True
console.log(a != 10); // La variable A es distinta a 10. False
console.log(a == '10'); // La variable A es igual a Diez.
                        //  True (== solamente te dice si es igual, pero no si es del mismo tipo de dato. Uno es Number y otro String)


console.log(a === '10'); // La triple igual comprueba si es el mismo tipo de dato y si es el mismo valor, entonces ahora si es False ya que es el mismo valor, pero no el mismo tipo de dato.

console.log(a !== '10'); // Si son distintos tipos de datos o de valor, usar esto. Te saldrá true.

